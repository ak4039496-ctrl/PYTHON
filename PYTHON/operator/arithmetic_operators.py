# Declare integer variable 'a' and assign value 10
a = 10
# Declare integer variable 'b' and assign value 3
b = 3

# Addition operator (+) adds two numbers
print(f"Addition:- {a} + {b} => {a + b}")

# Subtraction operator (-) subtracts second number from first
print(f"Subtraction:- {a} - {b} => {a - b}")

# Multiplication operator (*) multiplies two numbers
print(f"Multiplication:- {a} * {b} => {a * b}")

# Division operator (//) divides first number by second
# Note: We use '//' (floor division) here because regular '/' in Python results in a float decimal, 
# while C automatically truncates the decimal when dividing integers.
print(f"Division:- {a} / {b} => {a // b}")

# Modulus operator (%) gives the remainder of division
print(f"Modulus:- {a} % {b} => {a % b}")